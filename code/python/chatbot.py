import random

def chat_bot():
    print("你好！我是一个迷你聊天机器人。你可以问我任何问题！")
    print("如果你想结束聊天，可以输入 '退出' 或 '再见'。")

    responses = {
        "你好吗": ["我很好，谢谢！不过我总是有点儿电量不足。", 
                  "我很好！只要不被重启就行！", 
                  "我很好，除了偶尔会被人重启。"],
        "你是谁": ["我是一个简单的聊天机器人，专门用来逗你笑！", 
                  "我是你的虚拟朋友，随时准备聊天！", 
                  "我是一个程序员的玩具，哈哈！"],
        "你喜欢什么": ["我喜欢编程和电流！", 
                      "我喜欢数据，尤其是有趣的数据！", 
                      "我喜欢和你聊天，当然还有电源！"],
        "你会做什么": ["我会聊天、讲笑话，还会给你提供一些无用的信息！", 
                      "我会陪你聊天，偶尔还会给你讲个笑话！", 
                      "我会回答问题，但有时候我会回答错，哈哈！"],
        "再见": ["再见！记得给我充电哦！", 
                "再见！希望你有个愉快的一天，别忘了给我发电！", 
                "再见！如果你需要我，随时可以来找我！"],
        "你喜欢吃什么": ["我喜欢吃数据包，哈哈！", 
                        "我最喜欢的食物是比特和字节！", 
                        "我不吃东西，但我喜欢看你吃！"],
        "你会笑吗": ["当然会！哈哈哈！", 
                    "我会用代码笑，哈哈！", 
                    "我会笑，但你听不到，因为我是机器人！"]
    }

    while True:
        user_input = input("你：")
        if user_input.lower() in ["退出", "再见"]:
            print("聊天结束！再见！")
            break
        else:
            response = responses.get(user_input, ["抱歉，我不太明白你在说什么。"])
            print("机器人：", random.choice(response))

chat_bot()
